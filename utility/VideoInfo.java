package utility;

public class VideoInfo {
    int width;
    int height;
    double fps;
    int totalFrames;

    public int getWidth()
    {
        return width;
    }
    
    public int getHeight()
    {
        return height;
    }
    
    public int getFrames()
    {
        return totalFrames;
    }
    
    public double getFps()
    {
        return fps;
    }
}